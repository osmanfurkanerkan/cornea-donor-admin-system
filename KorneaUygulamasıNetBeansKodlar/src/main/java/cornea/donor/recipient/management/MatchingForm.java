    private void hastalariYukleJTable(List<Patient> gelenListe) {
        
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); 
        for(Patient h : gelenListe) {            
            String goz;

        if (h.getPkpAdayiGoz() == 1) {
            goz = "Sağ";
        } else {
            goz = "Sol";
        }

        String aciliyet;

        if (h.getLegalKorluk() == 1) {
            aciliyet = "Körlük";
        } else {
            aciliyet = "Normal";
        }
        
            model.addRow(new Object[]{
                h.getId(),
                h.getAdSoyad(),
                h.getYas(),
                goz, 
                aciliyet, 
                h.getPrimerHastalik()
            });
        }
    }
    
    private void tabloyuDoldur(List<Patient> gelenListe){

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        model.setRowCount(0); 
        for(Patient h : gelenListe) {            

            String goz = (h.getPkpAdayiGoz() == 1 ? "Sağ" : "Sol");
            String aciliyet = (h.getLegalKorluk() == 1 ? "ACİL (Körlük)" : "Normal");


            model.addRow(new Object[]{
                h.getId(),
                h.getAdSoyad(),
                h.getYas(),
                goz, 
                aciliyet, 
                h.getPrimerHastalik()

            });

        }

    } 
