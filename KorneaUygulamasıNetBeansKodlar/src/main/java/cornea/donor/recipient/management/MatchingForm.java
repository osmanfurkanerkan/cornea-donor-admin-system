    private List<Patient> rastgeleTurnuvaIleTop20Sec(List<Patient> tumHastalar) {
        List<Patient> havuz = new ArrayList<>(tumHastalar); 
        List<Patient> top20Listesi = new ArrayList<>();
        java.util.Random random = new java.util.Random();

        int secilecekKisiSayisi = Math.min(20, havuz.size());

        for (int i = 0; i < secilecekKisiSayisi; i++) {
            Patient suAnkiSampiyon = havuz.get(0);

            for (int j = 1; j < havuz.size(); j++) {
                Patient rakip = havuz.get(j);
                
                int modelKarari = random.nextInt(2); 
                
                if (modelKarari == 1) {
                    suAnkiSampiyon = rakip; 
                }
            }

            top20Listesi.add(suAnkiSampiyon);
            havuz.remove(suAnkiSampiyon);
        }

        return top20Listesi;
    }
