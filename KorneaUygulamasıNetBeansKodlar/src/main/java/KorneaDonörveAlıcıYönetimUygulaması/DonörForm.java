    private void button1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button1ActionPerformed
   
        String isim = txtAdSoyad.getText().trim();
        String yilStr = txtDogumYili.getText().trim();

        List<String> eksikAlanlar = new ArrayList<>();
        if (isim.isEmpty()) {
            eksikAlanlar.add("Donör Adı ve Soyadı");
        }
        if (yilStr.isEmpty()) {
            eksikAlanlar.add("Doğum Yılı");
        }

        if (!eksikAlanlar.isEmpty()) {
            String mesaj = "Lütfen aşağıdaki alanları doldurunuz:\n\n- " + String.join("\n- ", eksikAlanlar);
            JOptionPane.showMessageDialog(this, mesaj, "Eksik Bilgi", JOptionPane.WARNING_MESSAGE);
            return;
        }

     
        int yil = 0;
        try {
            yil = Integer.parseInt(yilStr);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Doğum yılı geçersiz! Lütfen sadece sayı giriniz (Örn: 1990).", "Hata", JOptionPane.ERROR_MESSAGE);
            return;
        }

       
        Integer endotel = null;
        String endotelStr = txtEndotel.getText().trim();

        if (!endotelStr.isEmpty()) {
            try {
                endotel = Integer.parseInt(endotelStr);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Endotel sayısı sadece rakam olmalıdır!", "Hata", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }


        try {        
            int cinsiyet;
            if (cbCinsiyet.getSelectedItem().toString().equalsIgnoreCase("erkek")) {
                cinsiyet = 0;
            } else {
                cinsiyet = 1;
            }

            int pkUygun;
            if (cbPkUygunluk.getSelectedItem().toString().equalsIgnoreCase("evet")) {
                pkUygun = 0;
            } else {
                pkUygun = 1;
            }

            Integer lamel = null;
            if (cbLamellerTip.getSelectedItem() != null) {
                String secimYazisi = cbLamellerTip.getSelectedItem().toString();

                if (secimYazisi.contains("Ön")) {
                    lamel = 1;
                } else if (secimYazisi.contains("Arka")) {
                    lamel = 2;
                }
            }


            Donör yeniDonor = new Donör();
            yeniDonor.setAdSoyad(isim);
            yeniDonor.setDogumYili(yil);
            yeniDonor.setCinsiyet(cinsiyet);
            yeniDonor.setEndotelSayisi(endotel);
            yeniDonor.setPkUygunluk(pkUygun);
            yeniDonor.setLamellerTip(lamel);


            DonörYöneticisi yonetici = new DonörYöneticisi();
            yonetici.yeniDonörEkle(yeniDonor);

            JOptionPane.showMessageDialog(this, "Donör başarıyla eklendi.");

            txtAdSoyad.setText("");
            txtDogumYili.setText("");
            txtEndotel.setText("");
            tabloyuYenileDonor();

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Beklenmeyen bir hata oluştu: " + e.getMessage());
        }
        
    }//GEN-LAST:event_button1ActionPerformed

    private void button2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button2ActionPerformed
        int seciliSatir = jTableDonor.getSelectedRow();
        if (seciliSatir == -1) {
            JOptionPane.showMessageDialog(this, "Lütfen silmek için bir donör seçin.");
            return;
        }
        int donorId = Integer.parseInt(jTableDonor.getValueAt(seciliSatir, 0).toString());

        int secim = JOptionPane.showConfirmDialog(this, 
                "Seçilen donör kaydını silmek istediğinize emin misiniz?", 
                "Silme Onayı", 
                JOptionPane.YES_NO_OPTION,     
                JOptionPane.WARNING_MESSAGE);  

        if (secim == JOptionPane.YES_OPTION) {
            DonörYöneticisi yonetici = new DonörYöneticisi();
            yonetici.donorSil(donorId);
            JOptionPane.showMessageDialog(this, "Donör başarıyla silindi.");
            tabloyuYenileDonor();

        }
    }//GEN-LAST:event_button2ActionPerformed

