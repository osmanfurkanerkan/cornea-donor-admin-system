    private void tabloyuYenileDonor() {
        DefaultTableModel model = (DefaultTableModel) jTableDonor.getModel();
        model.setRowCount(0);

        DonörYöneticisi yonetici = new DonörYöneticisi();
        List<Donör> donorler = yonetici.tumDonorleriListele();

        for (Donör d : donorler) {
            String lamelYazisi = "";
            
            if (d.getPkUygunluk() == 2) {
                Integer tip = d.getLamellerTip();
                if (tip != null) {
                    if (tip == 1) {
                        lamelYazisi = "ön Lameller";
                    } else if (tip == 2) {
                        lamelYazisi = "arka Lamel";
                    }
                }
            }

            model.addRow(new Object[]{
                d.getId(),
                d.getAdSoyad(),
                d.getDogumYili(),
                (d.getCinsiyet() == 0 ? "erkek" : "kadın"),
                d.getEndotelSayisi(),
                (d.getPkUygunluk() == 0 ? "evet" : "hayır"),
                lamelYazisi
            });
        }
    }


